function getSystemLanguage() {
  const browserLang = navigator.languages && navigator.languages[0]
    ? navigator.languages[0]
    : (navigator.language || 'en-US');

  if (languageMap[browserLang]) {
    return languageMap[browserLang];
  }

  const langCode = browserLang.split('-')[0];
  if (languageMap[langCode]) {
    return languageMap[langCode];
  }

  return 'en';
}

const I18n = {
  init: function (callback) {
    chrome.storage.local.get(['config'], function (result) {
      const config = result.config || {};
      if (config.system?.app_language) {
        currentLang = config.system.app_language;
      } else {
        currentLang = getSystemLanguage();
        if (!config.system) config.system = {};
        config.system.app_language = currentLang;
        chrome.storage.local.set({ config: config });
      }
      I18n.updatePage();
      if (callback) callback();
    });
  },

  setLanguage: function (lang) {
    if (translations[lang]) {
      currentLang = lang;
      chrome.storage.local.get(['config'], function (result) {
        const config = result.config || {};
        if (!config.system) config.system = {};
        config.system.app_language = lang;
        chrome.storage.local.set({ config: config });
      });
      I18n.updatePage();
    }
  },

  t: function (key) {
    return translations[currentLang][key] || key;
  },

  updatePage: function () {
    $('[data-i18n]').each(function () {
      const key = $(this).data('i18n');
      const translation = I18n.t(key);
      if ($(this).is('input, textarea')) {
      } else if ($(this).children().length === 0) {
        $(this).text(translation);
      } else {
        const nodes = $(this).contents();
        let found = false;
        for (let i = 0; i < nodes.length; i++) {
          if (nodes[i].nodeType === 3 && nodes[i].nodeValue.trim().length > 0) {
            nodes[i].nodeValue = translation;
            found = true;
            break;
          }
        }
      }
    });

    $('[data-i18n-placeholder]').each(function () {
      const key = $(this).data('i18n-placeholder');
      $(this).attr('placeholder', I18n.t(key));
    });

    $('[data-i18n-title]').each(function () {
      const key = $(this).data('i18n-title');
      $(this).attr('title', I18n.t(key));
    });

    $('[data-i18n-tooltip]').each(function () {
      const key = $(this).data('i18n-tooltip');
      $(this).attr('data-tooltip', I18n.t(key));
    });

    if ($('title[data-i18n]').length) {
      $('title[data-i18n]').text(I18n.t($('title[data-i18n]').data('i18n')));
    }
  },

  getCurrentLanguage: function () {
    return currentLang;
  }
};

window.I18n = I18n;
